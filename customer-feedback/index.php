                    if (this.value === 'yes') {

                        yesSection.classList.add('show');

                        noSection.classList.remove('show');



                        // Make yes section fields required

                        const yesFields = yesSection.querySelectorAll('input[type="radio"]');

                        yesFields.forEach(field => {

                            if (field.name === 'product_type' || field.name === 'billing_speed' || field.name === 'cashier_thanks') {

                                field.required = true;

                            }

                        });



                        // Remove required from no section

                        const noFields = noSection.querySelectorAll('input');

                        noFields.forEach(field => field.required = false);



                    } else if (this.value === 'no') {

                        noSection.classList.add('show');

                        yesSection.classList.remove('show');



                        // Make no section field required

                        document.getElementById('product_inquiry').required = true;



                        // Remove required from yes section

                        const yesFields = yesSection.querySelectorAll('input[type="radio"]');

                        yesFields.forEach(field => field.required = false);

                    }

                });

            });



